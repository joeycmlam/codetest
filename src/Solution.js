
export class Solution {

    constructor(nums, targets) {
        this.nums = nums;
        this.targets = targets;
    }

    /**
     * @param {number[]} nums
     * @param {number} target
     * @return {number}
     */
    twoSum = function() {

        const result = this.nums * this.targets;
        return result;
    };
}



